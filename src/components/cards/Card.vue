<template>
  <div v-if="shouldRenderCard" class="custom-card shadow-sm">
    <div class="card-image-wrapper" v-if="imagePosition === 'top' && imageSlot">
      <slot name="image"></slot>
    </div>

    <div class="card-header">
      <h6 v-if="title" class="titulo heading-h6">{{ title }}</h6>
    </div>

    <div class="content-wrapper">
      <h2 v-if="displayValue" class="value">{{ displayValue }}</h2>
      <p v-if="subtitle" class="textodescritivo body-small-medium">{{ subtitle }}</p>
    </div>

    <div class="card-image-wrapper" v-if="imagePosition === 'middle' && imageSlot">
      <slot name="image"></slot>
    </div>

    <div class="card-content" v-if="$slots.default">
      <slot></slot>
    </div>

    <div class="card-image-wrapper" v-if="imagePosition === 'bottom' && imageSlot">
      <slot name="image"></slot>
    </div>

    <div class="card-footer" v-if="$slots.footer">
      <slot name="footer"></slot>
    </div>
  </div>
</template>

<script>
export default {
  name: 'CardBase',
  props: {
    title: {
      type: String,
      required: false,
    },
    value: {
      type: String,
      required: false,
    },
    subtitle: {
      type: String,
      required: false,
    },
    imagePosition: {
      type: String,
      default: 'top',
      validator: value => ['top', 'middle', 'bottom'].includes(value),
    },
  },
  computed: {
    imageSlot() {
      return !!this.$slots.image;
    },

    // Determine if the card should be rendered at all
    shouldRenderCard() {
      // If no value is provided, or value is one of the error indicators, hide the card
      if (!this.value) {
        return this.$slots.default ? true : false;
      }

      const errorValues = [
        'Dados indisponíveis',
        'Dados não Disponíveis',
        'N/A',
        'Indisponível',
        '',
      ];

      // If value is in the error values list, only show if there's default content
      if (errorValues.includes(this.value.trim())) {
        return this.$slots.default ? true : false;
      }

      return true;
    },

    // Format the value for display
    displayValue() {
      if (!this.value) {return null;}

      const errorValues = [
        'Dados indisponíveis',
        'Dados não Disponíveis',
        'N/A',
        'Indisponível'
      ];

      // Don't display error values
      if (errorValues.includes(this.value.trim())) {
        return null;
      }

      return this.value;
    }
  },
};
</script>

